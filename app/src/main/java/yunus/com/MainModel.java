package yunus.com;

public class MainModel {
    private String nama;
    private String key;

    //constructur berfungsi untuk menjalankan model diaktivity
    public MainModel() {
    }


    //setter dan getter untuk mengolah data
    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;

    }


}
